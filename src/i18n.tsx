const englishFallback: TranslationMap = {
  ...dictionaries.de,
  ...dictionaries.en,
};

interface I18nContextValue {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  dir: "ltr" | "rtl";
  t: (key: string, values?: Record<string, string | number>) => string;
  text: (value?: Translation | null) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function interpolate(template: string, values?: Record<string, string | number>): string {
  if (!values) return template;
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template
  );
}

function getStoredLanguage(): AppLanguage {
  if (typeof window === "undefined") return "de";
  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "ar" || stored === "de" || stored === "en" ? stored : "de";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(getStoredLanguage);
  const dir = language === "ar" ? "rtl" : "ltr";

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = dir;
    window.localStorage.setItem(STORAGE_KEY, language);
  }, [dir, language]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, values?: Record<string, string | number>) => {
      const template = dictionaries[language][key] ?? englishFallback[key] ?? key;
      return interpolate(template, values);
    };

    const text = (value?: any) => {
      if (!value) return "";
      if (typeof value === "string") return value;
      return value[language] || value.de || value.en || value.ar || "";
    };

    return {
      language,
      setLanguage: setLanguageState,
      dir,
      t,
      text,
    };
  }, [dir, language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return value;
}
